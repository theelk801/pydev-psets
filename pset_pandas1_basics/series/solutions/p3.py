"""
Series operations
"""

# importing pandas module
import pandas as pd

# creating a series
data1 = pd.Series([5, 2, 3, 7], index=['a', 'b', 'c', 'd'])

# creating a series
data2 = pd.Series([1, 6, 4, 9], index=['a', 'b', 'd', 'e'])

# add data1 and data2
data1.add(data2, fill_value=0)

# subtract data1 and data2
data1.sub(data2, fill_value=0)
